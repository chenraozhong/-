//构造函数，进行初始值的写入
InitCls::InitCls(int m, int n) {
	m_people = m;
	m_resource = n;
}
//生成系统各资源量
void InitCls::InitRandomOfAvailable() {
	int my_number;
	srand((unsigned)time(NULL));
	m_Available.open("Available.txt");  //存储系统各资源量
	if (!m_Available.is_open()) {
		cout << "The m_Available don't open! error1!" << endl;
	}
	for (int i = 0; i < m_resource; i++) {
		my_number = rand() % 50 + 50;  //进行最大各资源量的随机生成（50~100)
		m_Available << my_number;
		if (i < m_resource - 1) {   //最后一个不加入空格符
			m_Available << " ";
		}
	}
	m_Available.close();
}
//生成客户最大资源数
void InitCls::InitRandomOfMax() {
	string c;
	int my_number1;
	vector<int> my_AvailableFinally;//存储提取的数字
	ifstream my_readofAvailable("Available.txt");
	if (!my_readofAvailable.is_open()) {
		cout << "The m_Available don't open! error2!" << endl;
	}
	while (getline(my_readofAvailable, c)) {//直接读取一行，把文本里的字符串输入到 c
		int x;//存储提取的数字
		stringstream s(c);//建立stringstream对象，初始化流内容为line所代表的字符串 
		while (s >> x) {//从line中一次读取数字存入x
			my_AvailableFinally.push_back(x);
		}
	}
	my_readofAvailable.close();
	m_Max.open("Max.txt");
	if (!m_Max.is_open()) {
		cout << "The Max don't open! error2！" << endl;
	}
	srand((unsigned)time(NULL));
	for (int i = 0; i < m_people; i++) {
		for (int j = 0; j < m_resource; j++) {
			my_number1 = rand() % my_AvailableFinally[j];//生成的资源数可能为零，如果用到需要判断
			m_Max << my_number1;
			if (j < m_resource - 1) {
				m_Max << " ";
			}
		}
		m_Max << "\n";//进行换行
	}
	m_Max.close();
}
//生成客户已占资源数以及占有时间数
void InitCls::InitRandomOfAllocationAndTime() {
	string c1;
	int my_number2,my_time;
	vector<int> my_MaxFinally;//存储其中的数字
	ifstream my_ReadOfMax("Max.txt");
	if (!my_ReadOfMax.is_open()) {
		cout << "The Max don't open! error3!" << endl;
	}
	while (getline(my_ReadOfMax,c1)) {//进行其中数字的提取
		int x1;
		stringstream s1(c1);
		while (s1 >> x1) {
			my_MaxFinally.push_back(x1);
		}
	}
	my_ReadOfMax.close();
	m_AllocationAndTime.open("AllocationAndTime.txt");//创建文件，存储客户已占资源数以及占有时间数
	srand((unsigned)time(NULL));   
	if (!m_AllocationAndTime.is_open()) {
		cout << "The AllocationAndTime don't open! error4! " << endl;
	}
	for (int i = 0; i < m_people; i++) {
		for (int j = 0; j < m_resource; j++) {//上一个函数由于生成的资源数可能为零，所以进行处理。
			if (my_MaxFinally[i * 4 + j] == 0) {
				my_number2 = 0;
			}
			else {
				my_number2 = rand() % my_MaxFinally[i * 4 + j];
			}
			my_time = rand() % 9+1;                 //对每个资源的时间不为零
			m_AllocationAndTime << my_number2 << " "<<my_time;
			if (j < m_resource - 1) {
				m_AllocationAndTime << " ";
			}
		}
		m_AllocationAndTime << "\n";   //进行换行
	}
	m_AllocationAndTime.close();
}